namespace DemoMetodai
{
    internal class Skaiciuotuvas
    {
        internal int Suma(int skaicius1, int skaicius2)
        {
            return skaicius1 + skaicius2;
        }

        internal static int Skirtumas(int skaicius1, int skaicius2)
        {
            return skaicius1 - skaicius2;
        }
    }
}
